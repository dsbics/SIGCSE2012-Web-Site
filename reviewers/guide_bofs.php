<h1>Guidelines for Reviewers of BOF Submissions</h1>
<div class="SectionHeader"><div class="Full">
<h2>General Remarks</h2>
</div></div>   
     
<p>Our goal in choosing Birds of a Feather (BOF) sessions is to provide a
selection of discussion sessions that will be attractive to attendees.
With this in mind, answers to the following questions are most useful
in BOF reviews:</p>
<ul>
 <li>Is the topic relevant to the symposium?</li>
 <li>Do you expect that the level of interest in the BOF would be high?</li>
 <li>Is the proposal suitable for a BOF (recall that BOFs are meant for open-ended discussion, and are explicitly not meant for either presentations or product demos)</li>
 <li>Are the particular presenters appropriate?  (Remember that BOF reviewing is not blind.)</li>
 <li>How could the authors improve the quality of the BOF, if it is accepted?</li>
</ul>
<p>Obviously this list is not exhaustive. The Program Committee and
authors will appreciate your views on other issues as well.</p>

